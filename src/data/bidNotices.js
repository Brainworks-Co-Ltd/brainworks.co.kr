export function getAllBidNotices() {
  return [...bidNotices].sort((a, b) => new Date(b.postedAt) - new Date(a.postedAt));
}

export function getLatestBidNotice() {
  return getAllBidNotices()[0] ?? null;
}

export function getBidNoticeBySlug(slug) {
  return bidNotices.find((notice) => notice.slug === slug) ?? null;
}

export function getBidNoticeSlugs() {
  return bidNotices.map((notice) => notice.slug);
}
